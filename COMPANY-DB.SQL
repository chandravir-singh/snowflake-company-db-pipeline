CREATE OR REPLACE DATABASE COMPANY_DB;
USE DATABASE COMPANY_DB;

CREATE OR REPLACE TABLE EMP_RAW (
    EMP_ID INT,
    NAME STRING,
    DEPARTMENT STRING,
    SALARY INT
);

CREATE OR REPLACE STAGE emp_stage;


COPY INTO EMP_RAW
FROM @EMP_STAGE
FILE_FORMAT = (TYPE = 'CSV' SKIP_HEADER = 1);

CREATE OR REPLACE PIPE emp_pipe
AUTO_INGEST = FALSE
AS
COPY INTO EMP_RAW
FROM @emp_stage
FILE_FORMAT = (TYPE = 'CSV');

ALTER PIPE emp_pipe REFRESH;

CREATE OR REPLACE STREAM emp_stream
ON TABLE EMP_RAW;

CREATE OR REPLACE TABLE EMP_SUMMARY (
    DEPARTMENT STRING,
    TOTAL_EMP INT,
    TOTAL_SALARY INT
);


CREATE OR REPLACE TASK emp_task
WAREHOUSE = COMPUTE_WH
SCHEDULE = '1 MINUTE'
AS
MERGE INTO EMP_SUMMARY tgt
USING (
    SELECT DEPARTMENT, 
           COUNT(*) AS total_emp,
           SUM(SALARY) AS total_salary
    FROM emp_stream
    GROUP BY DEPARTMENT
) src
ON tgt.DEPARTMENT = src.DEPARTMENT

WHEN MATCHED THEN
UPDATE SET 
    tgt.TOTAL_EMP = tgt.TOTAL_EMP + src.total_emp,
    tgt.TOTAL_SALARY = tgt.TOTAL_SALARY + src.total_salary

WHEN NOT MATCHED THEN
INSERT (DEPARTMENT, TOTAL_EMP, TOTAL_SALARY)
VALUES (src.DEPARTMENT, src.total_emp, src.total_salary);

ALTER TASK emp_task RESUME;

